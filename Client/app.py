def select_mode(key, mode):
    number = -1
    if 48 <= key <= 57:  # 0 ~ 9
        number = key - 48
    if key == 110:  # n
        mode = 0
    if key == 107:  # k
        mode = 1
    return number, mode

def calc_landmark_list(image, landmarks):
    image_width, image_height = image.shape[1], image.shape[0]

    landmark_point = []

    # Keypoint
    for _, landmark in enumerate(landmarks.landmark):
        landmark_x = min(int(landmark.x * image_width), image_width - 1)
        landmark_y = min(int(landmark.y * image_height), image_height - 1)
        # landmark_z = landmark.z

        landmark_point.append([landmark_x, landmark_y])

    return landmark_point


def pre_process_hand_history(image, hand_history):
    image_width, image_height = image.shape[1], image.shape[0]

    temp_hand_history = copy.deepcopy(hand_history)

    # Convert to relative coordinates
    base_x, base_y = 0, 0
    for index, point in enumerate(temp_hand_history):
        if index == 0:
            base_x, base_y = point[0], point[1]

        
        temp_hand_history[index][0] = (temp_hand_history[index][0] -
                                        base_x) / image_width
        temp_hand_history[index][1] = (temp_hand_history[index][1] -
                                        base_y) / image_height

    # Convert to a one-dimensional list
    temp_hand_history = list(
        itertools.chain.from_iterable(temp_hand_history))

    # print("Temp hand history: ",temp_hand_history)

    return temp_hand_history


def logging_csv(number, mode, frames_coordinate_list): # Save coordinates into .csv file
    if mode == 0:
        pass
    if mode == 1 and (0 <= number <= 9):
        for frame in frames_coordinate_list:
            csv_path = 'Client/model/hand_history_classifier/hand_history.csv'
            with open(csv_path, 'a', newline="") as f:
                writer = csv.writer(f)
                writer.writerow([number, *frame])
    return


